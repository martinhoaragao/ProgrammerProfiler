import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	    Scanner s = new Scanner(System.in);
	    while (s.hasNextInt())
	    {
	        int n = s.nextInt();
            if (n != 42)
            {
                System.out.println(n);
    	    } else
    	    {
                break;
    	    }
	    }
	}
}
