import java.util.*;
import java.lang.*;
import java.io.*;

/* Name of the class has to be "Main" only if the class is public. */
class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner in = new Scanner(System.in);
		int v = in.nextInt();
		while (v != 42) {
		    System.out.println(v);
		    v = in.nextInt();
		}
	}
}