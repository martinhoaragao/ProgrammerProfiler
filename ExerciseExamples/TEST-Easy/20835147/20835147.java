import java.util.*;
class Codechef
{
	public static void main(String [] args)
	{
	    Scanner scan = new Scanner(System.in);
	    
	    while(true)
	    {
	        int input = scan.nextInt();
	        if(input!=42)
	        {
	            System.out.println(input);
	        }
	        else
	        {
	            break;
	        }
	    }
	}
}